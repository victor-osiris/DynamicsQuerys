SELECT 
		S2.SOPNUMBE,
		S2.ITEMNMBR,
		S2.QUANTITY,
		MONTH(S1.DOCDATE) AS MES
FROM dbo.AAG20000 A1
    LEFT OUTER JOIN dbo.SOP30200 S1
        ON A1.DOCNUMBR = S1.SOPNUMBE LEFT OUTER JOIN dbo.SOP30300 S2 ON A1.DOCNUMBR = S2.SOPNUMBE
WHERE LEFT(S1.SOPNUMBE, 2) = 'FB'
      AND LEFT(S2.ITEMNMBR, 2) = 'AT' AND S1.DOCDATE BETWEEN '01/01/2017' AND '03/31/2017'
ORDER BY S2.ITEMNMBR